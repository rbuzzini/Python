# Python can do arithmetic with non numeric objects too.

# You can add strings with the + operator:
str1 = 'My name is '
str2 = 'Roberto'
print(str1 + str2)

#You can "multiply" strings with * operator:
print(str2*4)

# You can concatenate lists with + operator:
lst1 = [1, 2, 3]
lst2 = [4, 5, 6]
print(lst1 + lst2) 