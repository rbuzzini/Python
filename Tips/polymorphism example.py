# Polymorphism is a key concept in Object Oriented Programming.
# It literally means "multiple forms". It refers to use a single type entity
# to represent different types in different scenarios.

# Let's see a simple example with a built-in function
name = 'Joseph' # string
len(name)

names = ['Joseph', 'Jane'] # list
len(names)
# That's polymorphism in action: a single function knows how to handle different
# kind of objects.